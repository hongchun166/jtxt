package com.linkb.jstx.model.world_area;

public class CityBean {
    private String id;
    
    private String state_id;
    
    private String code;
    
    private String nameL;
    
    private String cname;
    
    private String lower_name;
    
    private String code_full;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getState_id() {
        return state_id;
    }

    public void setState_id(String state_id) {
        this.state_id = state_id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getNameL() {
        return nameL;
    }

    public void setNameL(String nameL) {
        this.nameL = nameL;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getLower_name() {
        return lower_name;
    }

    public void setLower_name(String lower_name) {
        this.lower_name = lower_name;
    }

    public String getCode_full() {
        return code_full;
    }

    public void setCode_full(String code_full) {
        this.code_full = code_full;
    }
}
