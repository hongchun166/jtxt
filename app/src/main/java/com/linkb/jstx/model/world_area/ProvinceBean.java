package com.linkb.jstx.model.world_area;


public class ProvinceBean {
    //stateBean
    private String id;
    
    private String country_id;
    
    private String code;
    
    private String name;
    
    private String cname;
    
    private String lower_name;
    
    private String code_full;
    
    private String area_id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCountry_id() {
        return country_id;
    }

    public void setCountry_id(String country_id) {
        this.country_id = country_id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getLower_name() {
        return lower_name;
    }

    public void setLower_name(String lower_name) {
        this.lower_name = lower_name;
    }

    public String getCode_full() {
        return code_full;
    }

    public void setCode_full(String code_full) {
        this.code_full = code_full;
    }

    public String getArea_id() {
        return area_id;
    }

    public void setArea_id(String area_id) {
        this.area_id = area_id;
    }
}
